def detect_mca_intent(message: str):
    """
    Detect MCA-specific intent with confidence score.
    Returns (intent, confidence) or (None, 0.0) if not MCA-related.
    """
    msg_norm = " " + " ".join(message.lower().replace("\n", " ").split()) + " "

    def _contains_phrase(phrase: str) -> bool:
        phrase_norm = " ".join(phrase.lower().split())
        if not phrase_norm:
            return False
        return f" {phrase_norm} " in msg_norm

    scores = {}

    for intent, keywords in MCA_INTENT_KEYWORDS.items():
        score = sum(1 for kw in keywords if _contains_phrase(kw))
        if score > 0:
            scores[intent] = score

    if not scores:
        return None, 0.0

    best_intent = max(scores, key=scores.get)
    confidence = min(scores[best_intent] / 2.0, 1.0)
    return best_intent, confidence