def detect_bsms_intent(message: str):
    """
    Detect BS-MS-specific intent with confidence score.
    Returns (intent, confidence) or (None, 0.0) if not BS-MS-related.
    """
    msg = message.lower()
    scores = {}

    for intent, keywords in BSMS_INTENT_KEYWORDS.items():
        score = sum(1 for kw in keywords if kw in msg)
        if score > 0:
            scores[intent] = score

    if not scores:
        return None, 0.0

    best_intent = max(scores, key=scores.get)
    confidence = min(scores[best_intent] / 2.0, 1.0)
    return best_intent, confidence